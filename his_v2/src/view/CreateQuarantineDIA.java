    private void create_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_ButtonActionPerformed
        if(noExpire_CheckBox.isSelected()) {
            int errorCode = quH.createQuarantine(selectedPerson, "");
            DialogMessage.showMessage(this, errorCode);
            
            if(errorCode == 0) {
                clean();
                dispose();
            }
        } else {
            String expire = expire_month_TextField.getText() + "/" + expire_year_TextField.getText();
            int errorCode = quH.createQuarantine(selectedPerson, expire);
            DialogMessage.showMessage(this, errorCode);
            
            if(errorCode == 0) {
                clean();
                dispose();
            }
        }
    }//GEN-LAST:event_create_ButtonActionPerformed

    private void noExpire_CheckBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_noExpire_CheckBoxActionPerformed
        if(noExpire_CheckBox.isSelected()) {
            expire_month_TextField.setText("");
            expire_year_TextField.setText("");
            expire_month_TextField.setEnabled(false);
            expire_year_TextField.setEnabled(false);
        } else {
            expire_month_TextField.setEnabled(true);
            expire_year_TextField.setEnabled(true);
        }
    }//GEN-LAST:event_noExpire_CheckBoxActionPerformed

