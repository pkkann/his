    private void list_ListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_list_ListValueChanged
        if (!evt.getValueIsAdjusting()) {
            if (!list_List.isSelectionEmpty()) {
                String selectionText = (String) list_List.getSelectedValue();
                String ids = "";
                for (int i = 0; i <= selectionText.length(); i++) {
                    if (selectionText.substring(i, i + 1).equals(":")) {
                        break;
                    }
                    String s = selectionText.substring(i, i + 1);
                    ids = ids + s;
                }
                int id = Integer.valueOf(ids);
                setUser(userHandler.getUser(id));
            }
        }
    }//GEN-LAST:event_list_ListValueChanged

    private void save_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_ButtonActionPerformed
        if (!firstname_TextField.getText().isEmpty() && !lastname_TextField.getText().isEmpty() && !email_TextField.getText().isEmpty()) {
            selectedUser.setFirstname(firstname_TextField.getText());
            selectedUser.setMiddlename(middlename_TextField.getText());
            selectedUser.setLastname(lastname_TextField.getText());
            selectedUser.setEmail(email_TextField.getText());
            selectedUser.setReserve(reserve_CheckBox.isSelected());
            selectedUser.setAdministrator(admin_CheckBox.isSelected());
            userHandler.saveUser(selectedUser);
            JOptionPane.showMessageDialog(this, "Brugeren blev gemt", "Gemt", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Alle felter skal vÃ¦re udfyldt", "Fejl", JOptionPane.ERROR_MESSAGE);
        }
                
    }//GEN-LAST:event_save_ButtonActionPerformed
